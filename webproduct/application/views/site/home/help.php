<h4 style="text-align:justify"><strong>Bước 1: Xem thông tin giỏ hàng</strong></h4>

<ul>
	<li style="text-align:justify">Hệ thống hiển thị các thông tin về sản phẩm bạn đã đặt mua như chủng loại, đơn giá, số lượng,...Tại đây bạn có thể thay đổi số lượng hàng hoá bằng cách nhập số lượng vào ô "Số lượng".</li>
	<li style="text-align:justify">Nếu muốn trả lại sản phẩm đã chọn (không mua nữa), bạn nhập “0” vào số lượng sản phẩm đó hoặc nhấn nút “Loại bỏ”.</li>
	<li style="text-align:justify">Nếu bạn có phiếu quà tặng hoặc phiếu khuyến mại, vui lòng chọn loại phiếu bạn có và nhập mã phiếu của bạn vào các ô tương ứng để nhận được khuyến mại.</li>
	<li style="text-align:justify">Nếu bạn muốn ủng hộ cho các chương trình xã hội của Tòhe bạn vui lòng chọn “Ủng hộ dự án” và nhập số tiền vào ô tương ứng.</li>
	<li style="text-align:justify">Nếu muốn tiếp tục xem thêm các hàng hóa khác, bạn nhấn vào ô “Tiếp tục mua hàng”, nếu không bạn nhấn vào ô “Thanh toán”.</li>
	<li style="text-align:justify">Mỗi lần thay đổi số lượng hàng hóa, bạn cần nhấn vào nút “Cập nhật” để hệ thống ghi nhớ sự thay đổi trong giỏ hàng của bạn trước khi bạn nhấn “Thanh toán”.</li>
</ul>

<h4 style="text-align:justify"><strong>Bước 2: Thông tin thanh toán và giao hàng</strong></h4>

<p style="margin-left:40px; text-align:justify"><em><strong>1. Nếu bạn đã có tài khoản tại tohe.vn</strong></em></p>

<ul>
	<li style="text-align:justify">Vui lòng đăng nhập tài khoản với địa chỉ email và mật khẩu bạn đã đăng ký với tohe.vn. Với việc tạo và đăng nhập với tài khoản đã đăng ký với tohe.vn, bạn sẽ được bỏ qua bước nhập thông tin khách hàng.</li>
</ul>

<p style="margin-left:40px; text-align:justify"><strong><em>2. Nếu bạn chưa có tài khoản tại tohe.vn</em></strong></p>

<ul>
	<li style="text-align:justify">Nếu muốn tạo tài khoản tại tohe.vn, bạn vui lòng chọn ô “Đăng kí tài khoản để mua sắm thuận tiện hơn”, ấn “Tiếp tục” và điền thông tin vào các ô tương ứng (Lưu ý : các ô có dấu “*” là các ô thông tin bắt buộc).</li>
	<li style="text-align:justify">Nếu bạn không muốn tạo tài khoản tại tohe.vn, vui lòng chọn ô “Mua hàng trực tuyến không cần đăng ký tài khoản” rồi ấn “Tiếp tục” và điền thông tin vào các ô tương ứng (Lưu ý: các ô có dấu “*” là các ô thông tin bắt buộc).</li>
	<li style="text-align:justify">Nếu bạn nhận hàng và thanh toán cùng một địa chỉ, nhấn vào ô “Địa chỉ thanh toán giống địa chỉ giao hàng” và nhấn “Tiếp tục”. Với việc nhấn ô này, bạn sẽ được bỏ qua bước điền “Chi tiết giao hàng” vì chúng tôi sẽ giao hàng đến địa chỉ bạn đã đăng ký tại bước thanh toán.</li>
	<li style="text-align:justify">Nếu địa chỉ giao hàng khác, bạn bỏ qua ô “Địa chỉ thanh toán giống địa chỉ giao hàng” chỉ nhấn “Tiếp tục”. Sau đó, bạn điền thông tin địa chỉ nhận hàng vào các ô tương ứng (Lưu ý: Các ô có dấu “*” là các ô thông tin bắt buộc).</li>
</ul>

<h4 style="text-align:justify"><strong>Bước 3: Phương thức vận chuyển</strong></h4>

<ul>
	<li style="text-align:justify">Chọn phương thức vận chuyển mà bạn muốn chúng tôi giao hàng đến cho bạn với các mức giá tương ứng chúng tôi đã đưa ra.&nbsp;&nbsp;</li>
	<li style="text-align:justify">Lưu ý: Chúng tôi chỉ nhận giao hàng trong ngày trong điều kiện địa chỉ giao hàng trong nội thành Hà Nội.</li>
	<li style="text-align:justify">Chọn “Tiếp tục”.</li>
</ul>

<h4 style="text-align:justify"><strong>Bước 4: Phương thức thanh toán</strong></h4>

<p style="text-align:justify">Vui lòng chọn phương thức thanh toán phù hợp với bạn.</p>




<h4 style="text-align:justify"><strong>Bước 5: Kết thúc</strong></h4>

<ul>
	<li style="text-align:justify">Sau khi bạn hoàn tất các bước thanh toán bạn sẽ nhận được 1 email xác nhận của chúng tôi trong vòng 24h (không kể ngày lễ, tết và ngày nghỉ) về đơn hàng của bạn đến email bạn đã đăng ký.</li>
</ul>

